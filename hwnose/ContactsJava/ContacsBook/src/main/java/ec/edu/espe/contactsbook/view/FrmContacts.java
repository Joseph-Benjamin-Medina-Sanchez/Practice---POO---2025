    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            readValues();
            int option = JOptionPane.showConfirmDialog(
                    rootPane,
                    "saving contact --> " + contact,
                    "SAVE CONTACTS?",
                    JOptionPane.YES_NO_CANCEL_OPTION
            );

            if (option == JOptionPane.YES_OPTION) {
                try {
                    contactRepository.save(contact);
                    JOptionPane.showMessageDialog(rootPane, "Your contact is saved in MongoDB --> " + contact);
                    emptyFields();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(
                            rootPane,
                            "Error saving contact in MongoDB:\n" + e.getMessage(),
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }
            } else if (option == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(rootPane, "Your data will be lost ", "", JOptionPane.WARNING_MESSAGE);
            } else {
                txtFirstName.requestFocus();
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void emptyFields() {
        txtFirstName.setText("");
        txtLastName.setText("");
        ftdBirthDate.setValue(null);
        cmbType.setSelectedIndex(0);
        radSexMale.setSelected(false);
        radSexFemale.setSelected(false);
        lstHobbies.clearSelection();
        txaComments.setText("");
    }

    private void readValues() {
        String firstName;
        String lastName;
        int age;
        String typeOfContact;
        String sex;
        ArrayList<String> hobbies;
        String comments;

        firstName = txtFirstName.getText().trim();
        if (firstName.isEmpty() || !firstName.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ\\s]+")) {
            throw new IllegalArgumentException("First Name is invalid. Use only letters.");
        }

        lastName = txtLastName.getText().trim();
        if (lastName.isEmpty() || !lastName.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ\\s]+")) {
            throw new IllegalArgumentException("Last Name is invalid. Use only letters.");
        }

        if (ftdBirthDate.getValue() == null) {
            throw new IllegalArgumentException("Please enter a valid birth date.");
        }

        Date dateInput = (Date) ftdBirthDate.getValue();
        LocalDate birthDate = dateInput.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate today = LocalDate.now();

        if (birthDate.isAfter(today)) {
            throw new IllegalArgumentException("Birth date cannot be in the future.");
        }

        age = Period.between(birthDate, today).getYears();

        if (age < 0) {
            throw new IllegalArgumentException("Invalid age calculation.");
        }

        typeOfContact = cmbType.getSelectedItem().toString();

        if (radSexMale.isSelected()) {
            sex = "Male";
        } else if (radSexFemale.isSelected()) {
            sex = "Female";
        } else {
            throw new IllegalArgumentException("Please select a sex.");
        }

        List<String> selectedHobbies = lstHobbies.getSelectedValuesList();
        hobbies = new ArrayList<>(selectedHobbies);

        comments = txaComments.getText();

        contact = new Contact(0, firstName, lastName, age, typeOfContact, sex, hobbies, comments);
    }

